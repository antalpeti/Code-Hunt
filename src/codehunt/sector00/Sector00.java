package codehunt.sector00;

/**
 * The class Sector00 is contains solution for Sector 00 section.
 */
public class Sector00 {

  /**
   * Negate input value.
   *
   * @param x
   * @return
   */
  public int captureCodeFragment01(int x) {
    return -x;
  }

  /**
   * Input value add by 1 or increment with 1.
   *
   * @param x
   * @return
   */
  public int captureCodeFragment02(int x) {
    // return x + 1;
    return ++x;
  }

  /**
   * Input value multiply by 2 .
   *
   * @param x
   * @return
   */
  public int captureCodeFragment03(int x) {
    return x * 2;
  }

  /**
   * Inputs sum.
   *
   * @param x
   * @param y
   * @return
   */
  public int captureCodeFragment04(int x, int y) {
    return x + y;
  }
}
